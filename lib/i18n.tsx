interface LanguageContextProps {
  language: LanguageCode;
  setLanguage: (lang: LanguageCode) => void;
  t: TranslationDict;
}

const LanguageContext = createContext<LanguageContextProps | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>("en");

  const setLanguage = (lang: LanguageCode) => {
    setLanguageState(lang);
    if (typeof window !== "undefined") {
      localStorage.setItem("user-lang", lang);
    }
  };

  useEffect(() => {
    if (typeof window === "undefined") return;

    // 1. Check local storage
    const saved = localStorage.getItem("user-lang") as LanguageCode;
    if (saved && translations[saved]) {
      setLanguageState(saved);
      return;
    }

    // 2. GEO / Location Auto-detection using browser preferences
    const browserLang = navigator.language.slice(0, 2).toLowerCase();
    
    // Map timezones to default languages for precise GEO targeting
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone || "";
    let detectedLang: LanguageCode = "en";

    if (browserLang === "es" || tz.includes("Madrid") || tz.includes("Barcelona") || tz.includes("Buenos_Aires")) {
      detectedLang = "es";
    } else if (browserLang === "pt" || tz.includes("Lisbon") || tz.includes("Sao_Paulo")) {
      detectedLang = "pt";
    } else if (browserLang === "sv" || tz.includes("Stockholm")) {
      detectedLang = "sv";
    } else if (browserLang === "no" || tz.includes("Oslo")) {
      detectedLang = "no";
    } else if (browserLang === "it" || tz.includes("Rome") || tz.includes("Milan")) {
      detectedLang = "it";
    } else if (browserLang === "fr" || tz.includes("Paris") || tz.includes("Zurich") || tz.includes("Geneva") || tz.includes("Brussels")) {
      detectedLang = tz.includes("Zurich") ? "de" : "fr";
    } else if (browserLang === "nl" || tz.includes("Amsterdam")) {
      detectedLang = "nl";
    } else if (browserLang === "de" || tz.includes("Berlin") || tz.includes("Vienna")) {
      detectedLang = "de";
    } else if (browserLang === "da" || tz.includes("Copenhagen")) {
      detectedLang = "da";
    } else if (browserLang === "fi" || tz.includes("Helsinki")) {
      detectedLang = "fi";
    } else if (["es", "pt", "sv", "no", "it", "fr", "nl", "de", "da", "fi"].includes(browserLang)) {
      detectedLang = browserLang as LanguageCode;
    }

    setLanguageState(detectedLang);
  }, []);

  const t = translations[language];

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}
